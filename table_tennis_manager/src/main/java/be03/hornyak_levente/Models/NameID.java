package be03.hornyak_levente.Models;

public class NameID {
    private Integer id;
    private String name;

    
    public NameID(Integer id, String name) {
        this.id = id;
        this.name = name;
    }


    public Integer getId() {
        return id;
    }


    public String getName() {
        return name;
    }

    

}
